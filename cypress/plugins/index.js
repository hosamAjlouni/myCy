/**
 * @type {Cypress.PluginConfig}
 */

module.exports = (on, config) => {

  // `on` is used to hook into various events Cypress emits
  // `config` is the resolved Cypress config

  if (config.userAgent === 'desktop') {
    config.userAgent = 'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/42.0.2311.135 Safari/537.36 Edge/12.246';
  }
  else if (config.userAgent === 'mobile') {
    config.userAgent = 'Mozilla/5.0 (Linux; Android 8.0.0; SM-G960F Build/R16NW) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/62.0.3202.84 Mobile Safari/537.36'
    config.viewportWidth = 375
  }

  return config
}
